#include <stdio.h>

int main(int argc, char const *argv[]) {

  int idade;
  int dias;

  // Ler a idade
  printf("Digite a sua idade: ");
  scanf("%d", &idade);

  // Calcular a quantidade de dias
  dias = idade * 365;

  // Imprimir o resultado
  printf("A quantidade em dias e: %d\n\n", dias);







  return 0;
}
